    fig.colorbar(im1, ax=axes[0])

    im2 = axes[1].imshow(y_pred, cmap="Blues")
    axes[1].imshow(heatmap_resized, cmap="jet", alpha=0.5)
    axes[1].set_title(f"Grad-CAM - {date_str}")
    fig.colorbar(im2, ax=axes[1])


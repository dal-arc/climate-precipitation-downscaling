def compute_LHD(Y_true, Y_pred, bin_min=1, bin_max=1050, bin_width=20):
    """
    حساب Log Histogram Distance (LHD) بين توزيعين
    """
    # 1. تسطيح القيم إلى 1D
    y_true_flat = Y_true.ravel()
    y_pred_flat = Y_pred.ravel()

    # 2. فلترة القيم > 1 mm/day فقط
    y_true_flat = y_true_flat[y_true_flat > 1]
    y_pred_flat = y_pred_flat[y_pred_flat > 1]

    # 3. تحديد الحاويات (bins)
    bins = np.arange(bin_min, bin_max + bin_width, bin_width)

    # 4. حساب histogram لكل توزيع
    γ_true, _ = np.histogram(y_true_flat, bins=bins)
    γ_pred, _ = np.histogram(y_pred_flat, bins=bins)

    # 5. إزالة الحاويات التي فيها < 10 قيم في كلا التوزيعين
    mask = (γ_true > 10) & (γ_pred > 10)
    γ_true = γ_true[mask]
    γ_pred = γ_pred[mask]

    # 6. حساب LHD باستخدام log10
    with np.errstate(divide='ignore', invalid='ignore'):
        log_ratio = 10 * np.log10(γ_true / γ_pred)
        log_ratio = np.nan_to_num(log_ratio, nan=0.0, posinf=0.0, neginf=0.0)

    return np.sqrt(np.mean(log_ratio ** 2))







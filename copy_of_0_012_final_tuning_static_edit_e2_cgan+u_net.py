def seasonal_mae(y_true, y_pred, season):
    true = y_true.sel(time=y_true['time.season'] == season).mean("time")
    pred = y_pred.sel(time=y_pred['time.season'] == season).mean("time")
    return np.abs(true - pred).mean().item()


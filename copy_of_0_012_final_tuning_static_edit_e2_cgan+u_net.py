def compute_cdd_1d(arr, threshold=1.0):
    is_dry = arr < threshold
    max_run = 0
    current_run = 0
    for dry in is_dry:
        if dry:
            current_run += 1
            max_run = max(max_run, current_run)
        else:
            current_run = 0
    return max_run

def calc_cdd_annual(xr_array, threshold=1.0):
    return xr_array.groupby("time.year").apply(
        lambda da: xr.apply_ufunc(
            compute_cdd_1d,
            da,
            kwargs={'threshold': threshold},
            input_core_dims=[["time"]],
            vectorize=True,
            dask="parallelized",
            output_dtypes=[int]
        )
    )

